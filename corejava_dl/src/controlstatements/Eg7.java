package controlstatements;

public class Eg7 {

	public static void main(String[] args) {
		// do while
		// if we want to execute the iteration only one time
		
		int i = 0;
		do {
			System.out.println(i);
			i++;
		}
			while(i <=5);
		
		int j = 5;
		do {
			System.out.println(j);
			j--;
		}
			while(j >=0);
		}
	}


