package controlstatements;

public class Eg5 {

	public static void main(String[] args) {// for lopp
		// when we know the number of conditions in advance we can use for loop
		for (int i = 0; i <= 5; i++){
		System.out.println(i);
		}
		for ( int j = 5; j>=0; j --){
			System.out.println(j);
		}
	}
}
