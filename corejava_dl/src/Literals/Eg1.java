package Literals;

public class Eg1 {

	public static void main(String[] args) {
		//Integer literal
		
		int a1 =10;
		System.out.println(a1);
		//a is variable;
		//int is datatype;
		//= is assignment opeerator;
		//10 is a value;
		
		// String literal
		
		String userName="Rakesh";
		System.out.println(userName);
		
		// float literal
		
		float f1=10.0f;
		System.out.println(f1);
		
		// boolean literal
		
		boolean bb = true;
		System.out.println(bb);
		
		
		// char Literal
		
		char ch1 = 'A';
		System.out.println(ch1);
		
		// 2 char
		
		char ch2 = '\u0000';
		System.out.println(ch2);
		
		// 3 char
		
		char ch3 = 99;
		System.out.println(ch3);
				
	}

}
