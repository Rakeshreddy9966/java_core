package coreJavaBasics;
//instance variable
public class Eg2 {

	byte b;
	int i;
	short s;
	long l;
	
	char c;
	
	boolean bb;
	
	float f;
	double d;
	
	public static void main(String[] args) {
		// create a object for instance method
		
		Eg2 eg2 = new Eg2();
		
		System.out.println(eg2.b);
		System.out.println(eg2.i);
		System.out.println(eg2.s);
		System.out.println(eg2.l);
		System.out.println(eg2.c);
		System.out.println(eg2.bb);
		System.out.println(eg2.f);
		System.out.println(eg2.d);
		

	}

}
